#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tail;

	if (!new)
		return ;
	tail = ft_lstlast(*lst);
	if (!*lst)
		*lst = new;
	else
		tail->next = new;
}
